use dusk_bls12_381::BlsScalar;

pub(crate) const fn zero_items<const H: usize, const A: usize>(
) -> [BlsScalar; H] {
    match A {
        2 => fill_items::<H>(&ZERO_ITEMS_2),
        4 => fill_items::<H>(&ZERO_ITEMS_4),
        8 => fill_items::<H>(&ZERO_ITEMS_8),
        _ => panic!("Only implemented for arity 2, 4 and 8"),
    }
}

const fn fill_items<const H: usize>(
    const_items: &[BlsScalar],
) -> [BlsScalar; H] {
    let mut array = [BlsScalar::zero(); H];
    let const_items_len = const_items.len();
    assert!(H < const_items_len, "Height must be smaller than 65");
    let mut i = 0;
    while i < H {
        // the last entry in const_item is the leaf, which we don't want to
        // include in the EMPTY_SUBTREES
        array[H - 1 - i] = const_items[const_items_len - 2 - i];
        i += 1;
    }
    array
}

